async def _send_frame(
    proc,
    frame_type: str,
    *,
    session_id: str,
    thread_id: str,
    request_id: str = "",
    payload: dict | None = None,
):
    frame = ProtocolFrame(
        seq=1,
        direction="client_to_server",
        type=frame_type,
        session_id=session_id,
        thread_id=thread_id,
        request_id=request_id,
        payload=payload or {},
    )
    proc.stdin.write(frame.to_json_line().encode("utf-8"))
    await proc.stdin.drain()


async def _read_frame(proc) -> ProtocolFrame:
    line = await asyncio.wait_for(proc.stdout.readline(), timeout=5)
    if not line:
        stderr = await proc.stderr.read()
        raise AssertionError(f"Server exited before frame. stderr={stderr.decode('utf-8')}")
    return frame_from_json(line.decode("utf-8"))


async def _stop_process(proc) -> None:
    if proc.returncode is None:
        proc.terminate()
        try:
            await asyncio.wait_for(proc.wait(), timeout=5)
        except asyncio.TimeoutError:
            proc.kill()
            await proc.wait()
    await _drain_process_pipes(proc)


async def _wait_process_exit_or_stop(proc, *, timeout: float) -> None:
    loop = asyncio.get_running_loop()
    deadline = loop.time() + timeout
    while proc.returncode is None and loop.time() < deadline:
        await asyncio.sleep(0.05)
    if proc.returncode is None:
        await _stop_process(proc)
        raise asyncio.TimeoutError


async def _disconnect_process_client(proc) -> None:
    if proc.stdin is not None and not proc.stdin.is_closing():
        proc.stdin.close()
        await proc.stdin.wait_closed()
    stdout_transport = getattr(proc.stdout, "_transport", None)
    if stdout_transport is not None:
        stdout_transport.close()


async def _drain_process_pipes(proc) -> None:
    for pipe in (proc.stdout, proc.stderr):
        if pipe is None:
            continue
        try:
            await asyncio.wait_for(pipe.read(), timeout=1)
        except (asyncio.TimeoutError, ValueError):
            continue


def _subprocess_pythonpath() -> str:
    repo_root = os.getcwd()
    paths = [str(os.path.join(repo_root, "XBotv2")), repo_root]
    existing = os.environ.get("PYTHONPATH")
    if existing:
        paths.append(existing)
    return os.pathsep.join(paths)
