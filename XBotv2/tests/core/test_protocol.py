class TestProtocolFrame:
    """ProtocolFrame serialization."""

    def test_frame_creation(self):
        """Create a basic frame."""
        frame = ProtocolFrame(
            seq=1,
            direction="server_to_client",
            type="hello_ok",
            session_id="s1",
            thread_id="t1",
            request_id="r1",
            payload={"server_name": "xbotv2"},
        )
        assert frame.protocol_version == PROTOCOL_VERSION
        assert frame.type == "hello_ok"
        assert frame.seq == 1

    def test_to_json_line(self):
        """Frame serializes to valid JSONL."""
        frame = ProtocolFrame(
            seq=1,
            direction="server_to_client",
            type="test",
            session_id="s1",
            thread_id="t1",
            request_id="r1",
        )
        line = frame.to_json_line()
        assert line.endswith("\n")
        parsed = json.loads(line)
        assert parsed["type"] == "test"
        assert parsed["session_id"] == "s1"

    def test_frame_from_json(self):
        """Deserialization round-trips."""
        frame = ProtocolFrame(
            seq=1,
            direction="client_to_server",
            type="user.message",
            session_id="s1",
            thread_id="t1",
            request_id="r1",
            payload={"content": "hello"},
        )
        line = frame.to_json_line()
        restored = frame_from_json(line)
        assert restored.type == "user.message"
        assert restored.payload["content"] == "hello"

    def test_frame_id_autogenerated(self):
        """frame_id is auto-generated if not provided."""
        frame = ProtocolFrame(
            seq=1,
            direction="server_to_client",
            type="test",
            session_id="s1",
            thread_id="t1",
            request_id="r1",
        )
        assert frame.frame_id.startswith("frame_")

    def test_ts_autogenerated(self):
        """ts is auto-generated."""
        frame = ProtocolFrame(
            seq=1,
            direction="server_to_client",
            type="test",
            session_id="s1",
            thread_id="t1",
            request_id="r1",
        )
        assert frame.ts  # Should be an ISO timestamp


class TestProtocolEncoder:
    """ProtocolEncoder maps internal events to wire frames."""

    def test_encoder_sequences(self):
        """Encoder increments seq with each frame."""
        encoder = ProtocolEncoder(session_id="s1", thread_id="t1")
        f1 = encoder.encode("test1", {"key": "val1"})
        f2 = encoder.encode("test2", {"key": "val2"})
        assert f1.seq == 1
        assert f2.seq == 2

    def test_encode_turn_started(self):
        """Convenience method works."""
        encoder = ProtocolEncoder(session_id="s1", thread_id="t1")
        frame = encoder.encode_turn_started(5)
        assert frame.type == "turn_started"
        assert frame.payload["turn"] == 5

    def test_encode_assistant_message(self):
        """Assistant message encoding."""
        encoder = ProtocolEncoder(session_id="s1", thread_id="t1")
        frame = encoder.encode_assistant_message("Hello!", [{"name": "shell", "args": {}}])
        assert frame.type == "assistant_message"
        assert frame.payload["content"] == "Hello!"
        assert len(frame.payload["tool_calls"]) == 1

    def test_encode_error(self):
        """Error encoding."""
        encoder = ProtocolEncoder(session_id="s1", thread_id="t1")
        frame = encoder.encode_error("Something went wrong", "test_error")
        assert frame.type == "error"
        assert frame.payload["code"] == "test_error"
        assert "Something went wrong" in frame.payload["message"]

    def test_session_id_in_all_frames(self):
        """All frames carry session/thread IDs."""
        encoder = ProtocolEncoder(session_id="my-session", thread_id="my-thread")
        frame = encoder.encode("test", {})
        assert frame.session_id == "my-session"
        assert frame.thread_id == "my-thread"

    def test_request_id_in_envelope_not_payload(self):
        """Request correlation stays in the protocol envelope."""
        encoder = ProtocolEncoder(session_id="s1", thread_id="t1")
        frame = encoder.encode_assistant_message("Hello!", request_id="req-123")

        assert frame.request_id == "req-123"
        assert "request_id" not in frame.payload

    def test_encode_usage_accumulates_totals(self):
        """Provider usage is encoded as a first-class protocol frame."""
        encoder = ProtocolEncoder(session_id="s1", thread_id="t1")

        first = encoder.encode_usage(
            {"prompt_tokens": 10, "completion_tokens": 4, "total_tokens": 14},
            request_id="req-1",
        )
        second = encoder.encode_usage(
            {"input_tokens": 3, "output_tokens": 2},
            request_id="req-2",
        )

        assert first.type == "usage"
        assert first.payload["delta"] == {
            "input_tokens": 10,
            "output_tokens": 4,
            "total_tokens": 14,
            "requests": 1,
        }
        assert second.payload["total"] == {
            "input_tokens": 13,
            "output_tokens": 6,
            "total_tokens": 19,
            "requests": 2,
        }


